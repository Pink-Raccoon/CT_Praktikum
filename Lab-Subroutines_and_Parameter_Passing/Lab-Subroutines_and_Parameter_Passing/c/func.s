; -------------------------------------------------------------------
; -- Constants
; -------------------------------------------------------------------
                AREA myCode, CODE, READONLY
                THUMB

; -------------------------------------------------------------------                    
; out_word
; - address in R0
; - value in R1
; -------------------------------------------------------------------   
out_word	      	PROC
					EXPORT out_word
					
					STR		R1, [R0]					; load value from R1 in address of R0
					BX		LR
				
					ENDP
						
; -------------------------------------------------------------------                    
; int_word
; - address in R0
; - result in R0
; -------------------------------------------------------------------   
in_word		      	PROC
					EXPORT in_word
						
					LDR		R0, [R0]					; read value from address R0 into R0
					
					BX		LR
				
					ALIGN
					ENDP
						
; -------------------------------------------------------------------
; -- End of file
; -------------------------------------------------------------------                      
                END
